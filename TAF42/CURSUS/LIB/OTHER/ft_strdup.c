#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	char	*dest;

	i = ft_strlen(str);
	dest = NULL;
	dest = malloc(sizeof(char) * (i + 1));
	if (!dest)
		return (NULL);
	i = -1;
	while (str[++i])
		dest[i] = str[i];
	dest[i] = '\0';
	return (dest);
}
